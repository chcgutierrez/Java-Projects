package modelo;

/**
 *
 * @author Chris
 */
public class TipoBD {

    private String codTipo;
    private String nomTipo;
    private String estTipo;
    private String fecTipo;
    private String obsTipo;

    public String getCodTipo() {
        return codTipo;
    }

    public void setCodTipo(String codTipo) {
        this.codTipo = codTipo;
    }

    public String getNomTipo() {
        return nomTipo;
    }

    public void setNomTipo(String nomTipo) {
        this.nomTipo = nomTipo;
    }

    public String getEstTipo() {
        return estTipo;
    }

    public void setEstTipo(String estTipo) {
        this.estTipo = estTipo;
    }

    public String getFecTipo() {
        return fecTipo;
    }

    public void setFecTipo(String fecTipo) {
        this.fecTipo = fecTipo;
    }

    public String getObsTipo() {
        return obsTipo;
    }

    public void setObsTipo(String obsTipo) {
        this.obsTipo = obsTipo;
    }

}
