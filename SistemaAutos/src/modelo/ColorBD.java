public class ColorBD {

    private String codColor;
    private String nomColor;
    private String estColor;
    private String fecColor;
    private String obsColor;

    public String getCodColor() {
        return codColor;
    }

    public void setCodColor(String codColor) {
        this.codColor = codColor;
    }

    public String getNomColor() {
        return nomColor;
    }

    public void setNomColor(String nomColor) {
        this.nomColor = nomColor;
    }

    public String getEstColor() {
        return estColor;
    }

    public void setEstColor(String estColor) {
        this.estColor = estColor;
    }

    public String getFecColor() {
        return fecColor;
    }

    public void setFecColor(String fecColor) {
        this.fecColor = fecColor;
    }

    public String getObsColor() {
        return obsColor;
    }

    public void setObsColor(String obsColor) {
        this.obsColor = obsColor;
    }

}
