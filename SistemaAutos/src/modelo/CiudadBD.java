package modelo;

/**
 *
 * @author Chris
 */
public class CiudadBD {
    
    private String codCiudad;
    private String nomCiudad;
    private String estCiudad;
    private String fecCiudad;
    private String obsCiudad;

    public String getCodCiudad() {
        return codCiudad;
    }

    public void setCodCiudad(String codCiudad) {
        this.codCiudad = codCiudad;
    }

    public String getNomCiudad() {
        return nomCiudad;
    }

    public void setNomCiudad(String nomCiudad) {
        this.nomCiudad = nomCiudad;
    }

    public String getEstCiudad() {
        return estCiudad;
    }

    public void setEstCiudad(String estCiudad) {
        this.estCiudad = estCiudad;
    }

    public String getFecCiudad() {
        return fecCiudad;
    }

    public void setFecCiudad(String fecCiudad) {
        this.fecCiudad = fecCiudad;
    }

    public String getObsCiudad() {
        return obsCiudad;
    }

    public void setObsCiudad(String obsCiudad) {
        this.obsCiudad = obsCiudad;
    }
        
}
