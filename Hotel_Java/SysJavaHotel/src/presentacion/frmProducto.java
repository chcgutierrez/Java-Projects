    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtNomProd.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Ingrese el Nombre del Producto", "Producto", JOptionPane.WARNING_MESSAGE);
            txtNomProd.requestFocus();
            return;
        }
        if (txtDescProd.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Ingrese la Descripción del Producto", "Producto", JOptionPane.WARNING_MESSAGE);
            txtDescProd.requestFocus();
            return;
        }
        if (cboUndMed.getSelectedIndex()==0) {
            JOptionPane.showMessageDialog(this, "Ingrese la Unidad de Medida del Producto", "Producto", JOptionPane.WARNING_MESSAGE);
            cboUndMed.requestFocus();
            return;
        }
        if (txtValVenta.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Ingrese el Valor del Producto", "Producto", JOptionPane.WARNING_MESSAGE);
            txtValVenta.requestFocus();
            return;
        }

        vProducto fDatosProd = new vProducto();
        fProducto nFuncionProd = new fProducto();

        fDatosProd.setNomProd(txtNomProd.getText());
        fDatosProd.setDescProd(txtDescProd.getText());
        fDatosProd.setUndmProd(String.valueOf(cboUndMed.getSelectedItem()));
        fDatosProd.setValvProd(Double.valueOf(txtValVenta.getText()));
        
        if (Accion.equals("Guardar")) {
            if (nFuncionProd.GuardarProd(fDatosProd)) {
                JOptionPane.showMessageDialog(this, "Producto Guardado Correctamente", "Producto", JOptionPane.INFORMATION_MESSAGE);
                Mostrar("");
                Inhabilitar();
            }

        } else if (Accion.equals("Editar")) {
            fDatosProd.setIdProd(Integer.valueOf(txtIDproducto.getText()));
            if (nFuncionProd.EditarProd(fDatosProd)) {
                JOptionPane.showMessageDialog(this, "Edición Realizada Correctamente", "Producto", JOptionPane.INFORMATION_MESSAGE);
                Mostrar("");
                Inhabilitar();
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void tblProdListadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProdListadoMouseClicked
        // TODO add your handling code here:
        btnGuardar.setText("Editar");
        Habilitar();
        btnBorrar.setEnabled(true);
        Accion = "Editar";

        int tFila = tblProdListado.rowAtPoint(evt.getPoint());
        txtIDproducto.setText(tblProdListado.getValueAt(tFila, 0).toString());
        txtNomProd.setText(tblProdListado.getValueAt(tFila, 1).toString());
        txtDescProd.setText(tblProdListado.getValueAt(tFila, 2).toString());
        cboUndMed.setSelectedItem(tblProdListado.getValueAt(tFila, 3).toString());
        txtValVenta.setText(tblProdListado.getValueAt(tFila, 4).toString());
        
    }//GEN-LAST:event_tblProdListadoMouseClicked

