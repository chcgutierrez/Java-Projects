    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        // TODO add your handling code here:
        if (!txtIDproducto.getText().equals("")) {
            int Confirmar = JOptionPane.showConfirmDialog(this, "¿Eliminar el Producto?", "Confirmar", 2);
            if (Confirmar == 0) {
                fProducto nFuncionProd = new fProducto();
                vProducto fDatosProd = new vProducto();

                fDatosProd.setIdProd(Integer.parseInt(txtIDproducto.getText()));
                nFuncionProd.BorrarProd(fDatosProd);
                JOptionPane.showMessageDialog(this, "Registro Borrado", "Producto", JOptionPane.INFORMATION_MESSAGE);
                Mostrar("");
                Inhabilitar();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un registro", "Producto", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

