    private void smnuResyConsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_smnuResyConsActionPerformed
        // TODO add your handling code here:
        frmReserva formaRsva = new frmReserva();
        dpnEscritorio.add(formaRsva);
        formaRsva.toFront();
        formaRsva.setVisible(true);
        frmReserva.txtIDempleado.setText(lblIDpersona.getText());
        frmReserva.txtNomEmpleado.setText(lblNombPer.getText() + " " + lblPrimApe.getText());
        frmReserva.codUsuario=Integer.valueOf(lblIDpersona.getText());
        Dimension tamaEscritorio = dpnEscritorio.getSize();
        Dimension tamaForm = formaRsva.getSize();
        formaRsva.setLocation((tamaEscritorio.width - tamaForm.width) / 2, (tamaEscritorio.height - tamaForm.height) / 2);
    }//GEN-LAST:event_smnuResyConsActionPerformed

