    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        if (txtBuscar.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Ingrese un criterio", "Producto", JOptionPane.WARNING_MESSAGE);
            txtBuscar.requestFocus();
            return;
        } else {
            Mostrar(txtBuscar.getText());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void tblProdListadoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProdListadoMousePressed
        // TODO add your handling code here:
        if (evt.getClickCount()==2) {
            
            int filaSel=tblProdListado.getSelectedRow();
            String codProdu, valorCod, preProd;
            codProdu=tblProdListado.getValueAt(filaSel, 0).toString();
            valorCod=tblProdListado.getValueAt(filaSel, 1).toString();
            preProd=tblProdListado.getValueAt(filaSel, 4).toString();
            
            frmConsumo.txtIDproducto.setText(codProdu);
            frmConsumo.txtNomProdu.setText(valorCod);
            frmConsumo.txtPreVenta.setText(preProd);
            this.dispose();
        }
    }//GEN-LAST:event_tblProdListadoMousePressed

