    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        try {
            DefaultTableModel Modelo;
            fEmpleado nFunLogin = new fEmpleado();
            vEmpleado DatosEmp = new vEmpleado();

            DatosEmp.setLoginEmp(txtUsuario.getText());
            DatosEmp.setClaveEmp(txtPassword.getText());

            Modelo = nFunLogin.Login(DatosEmp.getLoginEmp(), DatosEmp.getClaveEmp());
            tblUsuLogin.setModel(Modelo);

            if (nFunLogin.totFilas > 0) {
                JOptionPane.showMessageDialog(this, "Sesi칩n Iniciada", "Ingresar", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                frmInicio formaIni = new frmInicio();
                formaIni.toFront();
                formaIni.setVisible(true);
                frmInicio.lblIDpersona.setText(tblUsuLogin.getValueAt(0, 0).toString());
                frmInicio.lblNombPer.setText(tblUsuLogin.getValueAt(0, 1).toString());
                frmInicio.lblPrimApe.setText(tblUsuLogin.getValueAt(0, 2).toString());
                frmInicio.lblSegApe.setText(tblUsuLogin.getValueAt(0, 3).toString());
                frmInicio.lblAcceso.setText(tblUsuLogin.getValueAt(0, 4).toString());

                if (!frmInicio.lblAcceso.getText().equals("ADMINISTRADOR")) {
                    frmInicio.mnuArchivo.setEnabled(false);
                    frmInicio.mnuConfig.setEnabled(false);
                }

            } else {
                JOptionPane.showMessageDialog(this, "Acceso Denegado", "Ingresar", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

