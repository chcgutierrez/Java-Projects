        txtNumDocCliente.setText(tblListaClientes.getValueAt(tFila, 5).toString());
        txtDireCliente.setText(tblListaClientes.getValueAt(tFila, 6).toString());
        txtTelCliente.setText(tblListaClientes.getValueAt(tFila, 7).toString());        
        txtMailCliente.setText(tblListaClientes.getValueAt(tFila, 8).toString());
