    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        // TODO add your handling code here:
        if (!txtIDcliente.getText().equals("")) {
            int Confirmar = JOptionPane.showConfirmDialog(this, "¿Eliminar el Cliente?", "Confirmar", 2);
            if (Confirmar == 0) {
                fCliente nFunCliente = new fCliente();
                vCliente fDatCliente = new vCliente();

                fDatCliente.setIdPers(Integer.parseInt(txtIDcliente.getText()));
                nFunCliente.BorrarCliente(fDatCliente);
                JOptionPane.showMessageDialog(this, "Registro Borrado", "Cliente", JOptionPane.INFORMATION_MESSAGE);
                Mostrar("");
                Inhabilitar();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un registro", "Cliente", JOptionPane.WARNING_MESSAGE);
            tblListaClientes.requestFocus();
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

