    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtNomCliente.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Datos Incompletos", "Cliente", JOptionPane.WARNING_MESSAGE);
            txtNomCliente.requestFocus();
            return;
        }
        if (txtPrimApeCliente.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Datos Incompletos", "Cliente", JOptionPane.WARNING_MESSAGE);
            txtPrimApeCliente.requestFocus();
            return;
        }
        if (txtSegApeCliente.getText().length()==0) {
            JOptionPane.showMessageDialog(this, "Datos Incompletos", "Cliente", JOptionPane.WARNING_MESSAGE);
            txtSegApeCliente.requestFocus();
            return;
        }
        if (cboTipDocCliente.getSelectedIndex()==0) {
            JOptionPane.showMessageDialog(this, "Datos Incompletos", "Cliente", JOptionPane.WARNING_MESSAGE);
            cboTipDocCliente.requestFocus();
            return;
        }
        if (txtNumDocCliente.getText().length()==0) {
            JOptionPane.showMessageDialog(this, "Datos Incompletos", "Cliente", JOptionPane.WARNING_MESSAGE);
            txtNumDocCliente.requestFocus();
            return;
        }
        if (txtTelCliente.getText().length()==0) {
            JOptionPane.showMessageDialog(this, "Datos Incompletos", "Cliente", JOptionPane.WARNING_MESSAGE);
            txtTelCliente.requestFocus();
            return;
        }
        if (txtCodCliente.getText().length()==0) {
            JOptionPane.showMessageDialog(this, "Datos Incompletos", "Cliente", JOptionPane.WARNING_MESSAGE);
            txtCodCliente.requestFocus();
            return;
        }
        

        vCliente fDatCliente = new vCliente();
        fCliente nFunCliente = new fCliente();

        fDatCliente.setNomPers(txtNomCliente.getText());
        fDatCliente.setPrapePers(txtPrimApeCliente.getText());
        fDatCliente.setSegapePers(txtSegApeCliente.getText());
        fDatCliente.setTipdocPers(String.valueOf(cboTipDocCliente.getSelectedItem()));
        fDatCliente.setNumdocPers(txtNumDocCliente.getText());
        fDatCliente.setDirePers(txtDireCliente.getText());
        fDatCliente.setTelPers(txtTelCliente.getText());
        fDatCliente.setEmailPers(txtMailCliente.getText());
        fDatCliente.setCodCliente(txtCodCliente.getText());
        
        if (Accion.equals("Guardar")) {
            if (nFunCliente.GuardarCliente(fDatCliente)) {
                JOptionPane.showMessageDialog(this, "Cliente Guardado Correctamente", "Cliente", JOptionPane.INFORMATION_MESSAGE);
                Mostrar("");
                Inhabilitar();
            }

        } else if (Accion.equals("Editar")) {
            fDatCliente.setIdPers(Integer.valueOf(txtIDcliente.getText()));
            if (nFunCliente.EditarCliente(fDatCliente)) {
                JOptionPane.showMessageDialog(this, "Edición Realizada Correctamente", "Cliente", JOptionPane.INFORMATION_MESSAGE);
                Mostrar("");
                Inhabilitar();
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

