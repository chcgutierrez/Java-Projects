    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtNumero.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Ingrese el Número de la Habitación", "Habitación", JOptionPane.WARNING_MESSAGE);
            txtNumero.requestFocus();
            return;
        }
        if (txtDescripcion.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Ingrese la Descripción de la Habitación", "Habitación", JOptionPane.WARNING_MESSAGE);
            txtDescripcion.requestFocus();
            return;
        }
        if (txtCaracteristicas.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Ingrese la Descripción de la Habitación", "Habitación", JOptionPane.WARNING_MESSAGE);
            txtCaracteristicas.requestFocus();
            return;
        }
        if (txtPrecioDia.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Ingrese la Descripción de la Habitación", "Habitación", JOptionPane.WARNING_MESSAGE);
            txtPrecioDia.requestFocus();
            return;
        }

        vHabitacion fDatosHab = new vHabitacion();
        fHabitacion nFuncion = new fHabitacion();

        fDatosHab.setNumHab(txtNumero.getText());
        fDatosHab.setPisoHab(String.valueOf(cboPiso.getSelectedItem()));
        fDatosHab.setDescHab(txtDescripcion.getText());
        fDatosHab.setCaracHab(txtCaracteristicas.getText());
        fDatosHab.setPrediaHab(Double.valueOf(txtPrecioDia.getText()));
        fDatosHab.setTipoHab(String.valueOf(cboTipoHab.getSelectedItem()));
        fDatosHab.setEstHab(String.valueOf(cboEstado.getSelectedItem()));

        if (Accion.equals("Guardar")) {
            if (nFuncion.GuardarHab(fDatosHab)) {
                JOptionPane.showMessageDialog(this, "Habitacion Guardada Correctamente", "Habitación", JOptionPane.INFORMATION_MESSAGE);
                Mostrar("");
                Inhabilitar();
            }

        } else if (Accion.equals("Editar")) {
            fDatosHab.setIdHab(Integer.valueOf(txtIDhabitacion.getText()));
            if (nFuncion.EditarHab(fDatosHab)) {
                JOptionPane.showMessageDialog(this, "Edición Realizada Correctamente", "Habitación", JOptionPane.INFORMATION_MESSAGE);
                Mostrar("");
                Inhabilitar();
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        // TODO add your handling code here:
        if (!txtIDhabitacion.getText().equals("")) {
            int Confirmar = JOptionPane.showConfirmDialog(this, "¿Eliminar la Habitación?", "Confirmar", 2);
            if (Confirmar == 0) {
                fHabitacion nFuncion = new fHabitacion();
                vHabitacion fDatosHab = new vHabitacion();

                fDatosHab.setIdHab(Integer.parseInt(txtIDhabitacion.getText()));
                nFuncion.BorrarHab(fDatosHab);
                JOptionPane.showMessageDialog(this, "Registro Borrado", "Habitación", JOptionPane.INFORMATION_MESSAGE);
                Mostrar("");
                Inhabilitar();
            }
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione un registro", "Habitación", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

