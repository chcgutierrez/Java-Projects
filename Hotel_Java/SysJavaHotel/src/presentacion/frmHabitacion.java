    private void tblHabListadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHabListadoMouseClicked
        // TODO add your handling code here:
        btnGuardar.setText("Editar");
        Habilitar();
        btnBorrar.setEnabled(true);
        Accion = "Editar";

        int tFila = tblHabListado.rowAtPoint(evt.getPoint());
        txtIDhabitacion.setText(tblHabListado.getValueAt(tFila, 0).toString());
        txtNumero.setText(tblHabListado.getValueAt(tFila, 1).toString());
        cboPiso.setSelectedItem(tblHabListado.getValueAt(tFila, 2).toString());
        txtDescripcion.setText(tblHabListado.getValueAt(tFila, 3).toString());
        txtCaracteristicas.setText(tblHabListado.getValueAt(tFila, 4).toString());
        txtPrecioDia.setText(tblHabListado.getValueAt(tFila, 5).toString());
        cboTipoHab.setSelectedItem(tblHabListado.getValueAt(tFila, 6).toString());
        cboEstado.setSelectedItem(tblHabListado.getValueAt(tFila, 7).toString());
    }//GEN-LAST:event_tblHabListadoMouseClicked

