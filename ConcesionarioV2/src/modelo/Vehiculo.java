public class Vehiculo {
    
    String placa;
    String cityVeh;
    String marca;
    String tipoVeh;
    String modelo;
    String color;
    String motor;
    String estVeh;
    String obsVeh;
    String clienteVeh;

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getCityVeh() {
        return cityVeh;
    }

    public void setCityVeh(String cityVeh) {
        this.cityVeh = cityVeh;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getTipoVeh() {
        return tipoVeh;
    }

    public void setTipoVeh(String tipoVeh) {
        this.tipoVeh = tipoVeh;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getMotor() {
        return motor;
    }

    public void setMotor(String motor) {
        this.motor = motor;
    }

    public String getEstVeh() {
        return estVeh;
    }

    public void setEstVeh(String estVeh) {
        this.estVeh = estVeh;
    }

    public String getObsVeh() {
        return obsVeh;
    }

    public void setObsVeh(String obsVeh) {
        this.obsVeh = obsVeh;
    }

    public String getClienteVeh() {
        return clienteVeh;
    }

    public void setClienteVeh(String clienteVeh) {
        this.clienteVeh = clienteVeh;
    }
    
    
    
}
