package modelo;

public class Tipo {

    private String id_tipo;
    private String nom_tipo;
    private String est_tipo;
    private String obs_tipo;

    public String getId_tipo() {
        return id_tipo;
    }

    public void setId_tipo(String id_tipo) {
        this.id_tipo = id_tipo;
    }

    public String getNom_tipo() {
        return nom_tipo;
    }

    public void setNom_tipo(String nom_tipo) {
        this.nom_tipo = nom_tipo;
    }

    public String getEst_tipo() {
        return est_tipo;
    }

    public void setEst_tipo(String est_tipo) {
        this.est_tipo = est_tipo;
    }

    public String getObs_tipo() {
        return obs_tipo;
    }

    public void setObs_tipo(String obs_tipo) {
        this.obs_tipo = obs_tipo;
    }

}
