package modelo;

/**
 *
 * @author Chris
 */
public class Ciudad {

    private String id_ciudad;
    private String nom_ciudad;
    private String est_ciudad;
    private String obs_ciudad;

    public String getId_ciudad() {
        return id_ciudad;
    }

    public void setId_ciudad(String id_ciudad) {
        this.id_ciudad = id_ciudad;
    }

    public String getNom_ciudad() {
        return nom_ciudad;
    }

    public void setNom_ciudad(String nom_ciudad) {
        this.nom_ciudad = nom_ciudad;
    }

    public String getEst_ciudad() {
        return est_ciudad;
    }

    public void setEst_ciudad(String est_ciudad) {
        this.est_ciudad = est_ciudad;
    }

    public String getObs_ciudad() {
        return obs_ciudad;
    }

    public void setObs_ciudad(String obs_ciudad) {
        this.obs_ciudad = obs_ciudad;
    }

}
