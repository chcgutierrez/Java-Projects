
package modelo;

public class Color {

    private String id_color;
    private String nom_color;
    private String est_color;
    private String obs_color;

    public String getId_color() {
        return id_color;
    }

    public void setId_color(String id_color) {
        this.id_color = id_color;
    }

    public String getNom_color() {
        return nom_color;
    }

    public void setNom_color(String nom_color) {
        this.nom_color = nom_color;
    }

    public String getEst_color() {
        return est_color;
    }

    public void setEst_color(String est_color) {
        this.est_color = est_color;
    }

    public String getObs_color() {
        return obs_color;
    }

    public void setObs_color(String obs_color) {
        this.obs_color = obs_color;
    }

}
