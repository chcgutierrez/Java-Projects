package Modelo;

/**
 *
 * @author Chris
 */
public class Empleado {

    int id;
    String dni;
    String empNombre;
    String empTelefono;
    String estado;
    String user;

    public Empleado() {
    }

    public Empleado(int id, String dni, String empNombre, String empTelefono, String estado, String user) {
        this.id = id;
        this.dni = dni;
        this.empNombre = empNombre;
        this.empTelefono = empTelefono;
        this.estado = estado;
        this.user = user;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public String getEmpNombre() {
        return empNombre;
    }

    public void setEmpNombre(String empNombre) {
        this.empNombre = empNombre;
    }

    public String getEmpTelefono() {
        return empTelefono;
    }

    public void setEmpTelefono(String empTelefono) {
        this.empTelefono = empTelefono;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

}
